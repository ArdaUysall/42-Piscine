#include <stdlib.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	i;
	int	x;
	int	*result;

	if (min >= max)
	{
		*range = NULL;
		return (0);
	}
	x = max - min;
	result = malloc(sizeof(int) * (x));
	if (result == NULL)
	{
		*range = NULL;
		return (-1);
	}
	*range = result;
	i = 0;
	while (max > min)
	{
		result[i] = min;
		min++;
		i++;
	}
	return (i);
}
